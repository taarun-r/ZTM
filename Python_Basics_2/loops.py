# LOOPS 
# In Python key word is "FOR", it works with set list and tuples 

for item in "I love Coding":
    print(item)


for item in (1,2,3,4,5,6):
    print(item)

for item in [1,2,3,4,5,6]:
    print(item)

# Nested loops ! 

for item in (1,2,3,4,5,6):
    for letters in ["a", "b", "c", "d", "e", "f"]:
        print(item, letters)